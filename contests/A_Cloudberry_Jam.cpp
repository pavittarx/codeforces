#include <iostream>
using namespace std;

int main() {
  int cases;
  cin >> cases;

  while (cases != 0) {
    int jarsCount = 0;
    cin >> jarsCount;

    cout << jarsCount * 2 << endl;

    cases--;
  }

  return 0;
}